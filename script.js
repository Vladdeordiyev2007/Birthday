let timer = document.querySelector(".timer");
function update() {
    let now = new Date();//Текущее время
    let nextBirthday = now.getFullYear();
    let Birthday = new Date(nextBirthday, 0, 20, 0, 0, 0);//17 июля следующего года
    //Разница во времени в миллисекундах
    let timeDifference = Birthday - now;
    let days = Math.floor(timeDifference / (1000 * 60 * 60 * 24));
    let hours = Math.floor((timeDifference % (1000 * 60 * 60 *24)) / (1000 * 60 * 60));
    let minutes = Math.floor((timeDifference % (1000 * 60 * 60)) / (1000 * 60));
    let seconds = Math.floor((timeDifference % (1000 * 60)) / (1000));
    timer.innerHTML = `До дня рождения Ангелины осталось <span class="big"> ${days} </span> дней, <span class="big"> ${hours} </span> часов, <span class="big"> ${minutes} </span> минут, <span class="big"> ${seconds} </span> секунд!`;
}
setInterval(update, 1000);
