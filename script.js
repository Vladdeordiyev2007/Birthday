function calculateAge() {
            // Получаем значения из полей ввода
            const birthYear = parseInt(document.getElementById('birthYear').value);
            const futureYear = parseInt(document.getElementById('futureYear').value);
            const resultElement = document.getElementById('result');
            
            // Проверяем корректность ввода
            if (isNaN(birthYear) || isNaN(futureYear)) {
                resultElement.textContent = "Пожалуйста, введите корректные года";
                return;
            }
            
            if (futureYear < birthYear) {
                resultElement.textContent = "Будущий год должен быть больше года рождения";
                return;
            }
            
            // Вычисляем возраст
            const age = futureYear - birthYear;
            
            // Формируем правильное окончание для слова "год"
            let yearsText;
            const lastDigit = age % 10;
            const lastTwoDigits = age % 100;
            
            if (lastTwoDigits >= 11 && lastTwoDigits <= 14) {
                yearsText = "лет";
            } else {
                switch(lastDigit) {
                    case 1:
                        yearsText = "год";
                        break;
                    case 2:
                    case 3:
                    case 4:
                        yearsText = "года";
                        break;
                    default:
                        yearsText = "лет";
                }
            }
            
            // Выводим результат
            resultElement.textContent = `В ${futureYear} году вам исполнится ${age} ${yearsText}.`;
        }